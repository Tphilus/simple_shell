/**
 * is_delim - this checkn the delimeter
 * @c: character help to check
 * @delim: the delimeter obviously
 * Return: 1 ? true : 0
 */
int is_delim(char c, char *delim)
{
	while (*delim)
		if (*delim++ == c)
			return (1);
	return (0);
}

/**
 *_isalpha - check the was the program is altered
 *@c: char to the input in the shell
 *Return: 1 ? _isalpha : 0
 */

int _isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	else
		return (0);
}

