/**
 **_memset - fills memory with constant
 *@s: pointer to memory field
 *@b: byte to fill *s 
 *@n: amount of bytes to fill
 *Return: (s) a pointer to the memory fields
 */
char *_memset(char *s, char b, unsigned int n)
{
	unsigned int i;

	for (i = 0; i < n; i++)
		s[i] = b;
	return (s);
}

/**
 * ffree - liberates string
 * @pp: string of strings
 */
void ffree(char **pp)
{
	char **a = pp;

	if (!pp)
		return;
	while (*pp)
		free(*pp++);
	free(a);
}

